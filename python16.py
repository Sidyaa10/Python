#Dictionaries
#Dictionary is a special structure in python which allow us to store info in key value pairs.

monthConversions ={
    "Jan": "January",
    "Feb": "February",
    "Mar": "March",
    "Apr": "April",
}
#this is how to access a key value pair
print(monthConversions["Jan"])
print(monthConversions.get("Apr"))
