#python can handle any kind of math equation
#+(plus), -(minus), *(multipy), //(divide), % (modulos)
num=6
print(num)
print(abs(num))#gives absolute valus of the number
print(pow(num,2))#squares the number basically 6^2
print(max(num,-num))#tells the bigger number
print(min(num,-num))#tells the smallest number
num1=3.15
print(round(num1))#roundsup the number

num2=4.01
print(floor(num2))#removes everything after decimal point
print(ceil(num2))#rounds up the number to the next number
print(sqrt(num2))#tells the square root of 4.01
