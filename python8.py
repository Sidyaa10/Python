#lists

friends = ["Apollo","Heracles","Ares","Hades","Zeus"]
#to print the entire list
print(friends)

#to print a specific index from the list
print(friends[1])

#to print the last index in the lists
print(friends[-1])

#to print all the indexes after 1 and 1 itself
print(friends[1:])

#to print all the indexes from 2 to 4
print(friends[2:4])

#to update the input at index 3 to another name
friends[3]="Helios"
print(friends[3])